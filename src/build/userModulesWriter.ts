import * as path from "node:path";
import { DEFAULT_SSL_BUFFER_SIZE, type NodemcuConfig } from "../config/nodemcuIni";

const GUARD_BEGIN = "#ifndef __USER_MODULES_H__\n#define __USER_MODULES_H__\n";
const GUARD_END = "#endif\t/* __USER_MODULES_H__ */\n";

const KNOWN_MODULES = new Set([
  "adc", "ads1115", "adxl345", "am2320", "apa102", "bit", "bloom", "bmp085",
  "bme280", "bme280_math", "bme680", "coap", "color_utils", "cron", "crypto",
  "dcc", "dht", "encoder", "enduser_setup", "file", "gdbstub", "gpio",
  "gpio_pulse", "hdc1080", "hmc5883l", "http", "hx711", "i2c", "l3g4200d",
  "mcp4725", "mdns", "mqtt", "net", "node", "ow", "pcm", "perf", "pipe",
  "pixbuf", "pwm", "pwm2", "rfswitch", "rotary", "rtcfifo", "rtcmem", "rtctime",
  "si7021", "sigma_delta", "sjson", "sntp", "softuart", "somfy", "spi",
  "struct", "switec", "tcs34725", "tm1829", "tls", "tmr", "tsl2561", "uart",
  "u8g2", "ucg", "websocket", "wiegand", "wifi", "wifi_monitor", "wps",
  "ws2801", "ws2812", "ws2812_effects", "xpt2046",
]);

// Some modules cannot be built in isolation. Enabling a module here force-enables
// its dependencies in user_modules.h regardless of the [c_modules] selection, so a
// user who "just adds tls" gets a firmware that actually compiles and links.
// `tls` requires `http` (and `net`, already mandatory) per docs/modules/tls.md.
export const MODULE_DEPENDENCIES: Record<string, string[]> = {
  tls: ["http"],
};

function upperDefine(name: string): string {
  return `LUA_USE_MODULES_${name.toUpperCase()}`;
}

export interface WriteResult {
  written: string[];
  removed: string[];
  extras: string[];
}

export const MANDATORY_C_MODULES = new Set(["file", "gpio", "net", "node", "tmr", "uart", "wifi"]);

export function generateUserModulesHeader(config: NodemcuConfig): string {
  const selected = new Set<string>();
  for (const [name, enabled] of Object.entries(config.c_modules)) {
    if (!enabled) continue;
    const key = name.toLowerCase();
    if (KNOWN_MODULES.has(key)) {
      selected.add(key);
    }
  }

  for (const name of MANDATORY_C_MODULES) {
    if (KNOWN_MODULES.has(name)) {
      selected.add(name);
    }
  }

  // Pull in dependencies of whatever is selected (e.g. tls -> http).
  for (const mod of Array.from(selected)) {
    for (const dep of MODULE_DEPENDENCIES[mod] ?? []) {
      if (KNOWN_MODULES.has(dep)) {
        selected.add(dep);
      }
    }
  }

  const lines: string[] = [];
  lines.push("// Auto-generated by nodemcu-vscode. Do not edit by hand.");
  lines.push("// Source of truth: nodemcu.ini [c_modules] section.");
  lines.push(GUARD_BEGIN.trimEnd());
  lines.push("");
  lines.push("#ifndef LUA_CROSS_COMPILER");
  lines.push("");

  const sorted = Array.from(KNOWN_MODULES).sort();
  for (const mod of sorted) {
    const define = upperDefine(mod);
    if (selected.has(mod)) {
      lines.push(`#define ${define}`);
    } else {
      lines.push(`//#define ${define}`);
    }
  }

  lines.push("");
  lines.push("#endif  /* LUA_CROSS_COMPILER */");
  lines.push(GUARD_END.trimEnd());
  return lines.join("\n") + "\n";
}

export function writeUserModulesHeader(headerPath: string, config: NodemcuConfig): WriteResult {
  const before = readSelectedModules(headerPath);
  const next = generateUserModulesHeader(config);
  fs.mkdirSync(path.dirname(headerPath), { recursive: true });
  fs.writeFileSync(headerPath, next, "utf-8");
  const after = readSelectedModules(headerPath);
  return {
    written: after.filter((m) => !before.includes(m)),
    removed: before.filter((m) => !after.includes(m)),
    extras: after.filter((m) => !KNOWN_MODULES.has(m)),
  };
}

export function readSelectedModules(headerPath: string): string[] {
  if (!fs.existsSync(headerPath)) return [];
  const content = fs.readFileSync(headerPath, "utf-8");
  const regex = /^#define\s+LUA_USE_MODULES_([A-Z0-9_]+)/gm;
  const out: string[] = [];
  let m: RegExpExecArray | null;
  while ((m = regex.exec(content)) !== null) {
    out.push(m[1].toLowerCase());
  }
  return out;
}

export function diffSelectedModules(before: string[], after: string[]): { added: string[]; removed: string[] } {
  const beforeSet = new Set(before);
  const afterSet = new Set(after);
  return {
    added: after.filter((m) => !beforeSet.has(m)),
    removed: before.filter((m) => !afterSet.has(m)),
  };
}

export function isCModulesConfigChanged(headerPath: string, config: NodemcuConfig): boolean {
  if (!fs.existsSync(headerPath)) return true;
  const currentContent = fs.readFileSync(headerPath, "utf-8");
  const expectedContent = generateUserModulesHeader(config);
  return currentContent !== expectedContent;
}

export function isTlsEnabled(config: NodemcuConfig): boolean {
  for (const [name, enabled] of Object.entries(config.c_modules)) {
    if (enabled && name.toLowerCase() === "tls") return true;
  }
  return false;
}

/**
 * Toggle the SSL client support in `app/include/user_config.h` to match whether
 * the `tls` module is enabled. The TLS module needs `CLIENT_SSL_ENABLE` defined
 * (commented out by default) and a larger `SSL_BUFFER_SIZE`. Pure string
 * transform so it stays unit-testable; only the two known lines are touched.
 */
export function setUserConfigSsl(content: string, enabled: boolean, bufferSize: number = DEFAULT_SSL_BUFFER_SIZE): string {
  let out = content;
  const clientRe = /^[ \t]*\/*[ \t]*#define[ \t]+CLIENT_SSL_ENABLE\b.*$/m;
  if (clientRe.test(out)) {
    out = out.replace(clientRe, enabled ? "#define CLIENT_SSL_ENABLE" : "//#define CLIENT_SSL_ENABLE");
  }
  if (enabled) {
    const size = Number.isFinite(bufferSize) && bufferSize > 0 ? Math.floor(bufferSize) : DEFAULT_SSL_BUFFER_SIZE;
    const bufRe = /^[ \t]*\/*[ \t]*#define[ \t]+SSL_BUFFER_SIZE\b.*$/m;
    if (bufRe.test(out)) {
      out = out.replace(bufRe, `#define SSL_BUFFER_SIZE ${size}`);
    }
  }
  return out;
}

/** Apply {@link setUserConfigSsl} to a file on disk. Returns true if it changed. */
export function writeUserConfigSsl(headerPath: string, enabled: boolean, bufferSize: number = DEFAULT_SSL_BUFFER_SIZE): boolean {
  if (!fs.existsSync(headerPath)) return false;
  const before = fs.readFileSync(headerPath, "utf-8");
  const after = setUserConfigSsl(before, enabled, bufferSize);
  if (after === before) return false;
  fs.writeFileSync(headerPath, after, "utf-8");
  return true;
}

/**
 * Toggle the LFS (Lua Flash Store) partition size in `app/include/user_config.h`.
 * The firmware ships `LUA_FLASH_STORE` commented out (the `#ifndef` fallback then
 * defines it as `0x0` = disabled). When `sizeBytes > 0` we activate the user line
 * with the requested partition size; when `0` we comment it back out so the
 * fallback restores the disabled default. Pure string transform (unit-testable),
 * touching only the single user-config `#define LUA_FLASH_STORE` line. The
 * `#  define LUA_FLASH_STORE 0x0` fallback (note the space after `#`) is left
 * alone — the regex requires a contiguous `#define`, so it never matches there.
 * `LUA_INIT_STRING` is intentionally not touched: init.lua stays the SPIFFS
 * bootstrap that requires LFS modules.
 */
export function setUserConfigLfs(content: string, sizeBytes: number): string {
  const lfsRe = /^[ \t]*\/*[ \t]*#define[ \t]+LUA_FLASH_STORE\b.*$/m;
  if (!lfsRe.test(content)) return content;
  if (sizeBytes > 0) {
    const size = Math.floor(sizeBytes);
    return content.replace(lfsRe, `#define LUA_FLASH_STORE                   0x${size.toString(16)}`);
  }
  return content.replace(lfsRe, "//#define LUA_FLASH_STORE                   0x10000");
}

/** Apply {@link setUserConfigLfs} to a file on disk. Returns true if it changed. */
export function writeUserConfigLfs(headerPath: string, sizeBytes: number): boolean {
  if (!fs.existsSync(headerPath)) return false;
  const before = fs.readFileSync(headerPath, "utf-8");
  const after = setUserConfigLfs(before, sizeBytes);
  if (after === before) return false;
  fs.writeFileSync(headerPath, after, "utf-8");
  return true;
}

// Baud rates the firmware UART driver recognizes (NodeMCU app/include/driver/uart.h
// UartBautRate enum). BIT_RATE_DEFAULT must name one of these constants, whose value
// equals the baud, so an out-of-set baud would name an undefined macro and fail to
// compile. We map the nodemcu.ini baud onto the nearest valid constant.
const FIRMWARE_BIT_RATES = [
  300, 600, 1200, 2400, 4800, 9600, 19200, 38400, 57600, 74880,
  115200, 230400, 460800, 921600, 1843200, 3686400,
];
const DEFAULT_BIT_RATE = 115200;

/**
 * Sync the firmware boot UART speed in `app/include/user_config.h` with the
 * nodemcu.ini `[nodemcu] baud`. The firmware ships `#define BIT_RATE_DEFAULT
 * BIT_RATE_115200`; without this the device always boots at the firmware default
 * and ignores the configured baud. `baud` is mapped onto the nearest valid
 * `BIT_RATE_*` constant (the enum value equals the baud) so an arbitrary number
 * never names an undefined macro. Pure string transform; only the single
 * `#define BIT_RATE_DEFAULT` line is touched (the `//#define BIT_RATE_AUTOBAUD`
 * line is left alone since the regex requires a contiguous `#define`).
 */
export function setUserConfigBitRate(content: string, baud: number): string {
  const rateRe = /^[ \t]*#define[ \t]+BIT_RATE_DEFAULT\b.*$/m;
  if (!rateRe.test(content)) return content;
  const rate = Number.isFinite(baud) && baud > 0
    ? FIRMWARE_BIT_RATES.reduce((a, b) => (Math.abs(b - baud) < Math.abs(a - baud) ? b : a))
    : DEFAULT_BIT_RATE;
  return content.replace(rateRe, `#define BIT_RATE_DEFAULT BIT_RATE_${rate}`);
}

/** Apply {@link setUserConfigBitRate} to a file on disk. Returns true if it changed. */
export function writeUserConfigBitRate(headerPath: string, baud: number): boolean {
  if (!fs.existsSync(headerPath)) return false;
  const before = fs.readFileSync(headerPath, "utf-8");
  const after = setUserConfigBitRate(before, baud);
  if (after === before) return false;
  fs.writeFileSync(headerPath, after, "utf-8");
  return true;
}

